    public void header(){
        try{
            sql = "SELECT uid FROM presensi ORDER BY time DESC LIMIT 1";
            rs = stat.executeQuery(sql);
            if(rs.next()){
                S = rs.getString(1);
                try{
                    sql = "SELECT nama FROM rfiddatabase WHERE uid='"+S+"'";
                    rs = stat.executeQuery(sql);
                    if(rs.next()){
                        nama.setText(rs.getString("nama"));
                    }
                }catch(Exception e){
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
