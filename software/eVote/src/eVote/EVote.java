        Scan scan = new Scan();
        JFrame scanwindow = scan.window;
        int a = JOptionPane.showConfirmDialog(null, "You are about to choose "+OPT+"\nAre you sure?");
        if(a == JOptionPane.YES_OPTION){
            try{
                sql = "UPDATE `rfiddatabase` SET `hadir`='1' WHERE uid='"+S+"'";
                stat.executeUpdate(sql);
                sql = "INSERT INTO `pemilu`(`pilihan`) VALUES ('"+opt+"')";
                stat.executeUpdate(sql);
                
                JOptionPane.showMessageDialog(null, "Terimakasih, "+nama.getText()+".\n Anda telah memilih "+OPT+".\n Silakan tekan oke dan kembali.");
            
                scanwindow.setVisible(true);            
                this.dispose();
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
